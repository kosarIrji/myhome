import React from "react";

type Variant = "primary" | "secondary" | "ghost";

interface BaseProps {
  title: string;
  variant?: Variant;
  icon?: React.ReactNode;
  className?: string;
}

interface ButtonAsButton extends BaseProps {
  as: "button";
  type?: "button" | "submit" | "reset";
  onClick?: React.MouseEventHandler<HTMLButtonElement>;
}

interface ButtonAsLink extends BaseProps {
  as: "link";
  href: string;
  target?: React.HTMLAttributeAnchorTarget;
  rel?: string;
}

type ButtonProps = ButtonAsButton | ButtonAsLink;

const variantStyles: Record<Variant, string> = {
  primary: "bg-(--back)/70 shadow-xl hover:shadow-2xl hover:bg-(--back)/90",
  secondary: "border border-gray-600 shadow-xl hover:shadow-2xl",
  ghost: "bg-transparent text-white hover:bg-white/10",
};

export default function Button(props: ButtonProps) {
  const { title, variant = "primary", icon, className = "" } = props;

  const combined = `
    flex items-center justify-center gap-3 px-5 py-2 rounded-xl font-medium
    transition duration-200 ${variantStyles[variant]} ${className}
  `;

  const content = (
    <>
      <span>{title}</span>
      {icon && <span>{icon}</span>}
    </>
  );

  if (props.as === "button") {
    return (
      <button
        type={props.type ?? "button"}
        onClick={props.onClick}
        className={combined}
      >
        {content}
      </button>
    );
  }

  return (
    <Link
      href={props.href}
      target={props.target}
      rel={props.rel}
      className={combined}
    >
      {content}
    </Link>
  );
}
